"""
YAML generator using the existing Liquid template.

Converts schedule data into the JSON format expected by the Liquid template,
then renders it to produce Azure DevOps pipeline YAML files.
"""

import json
import os
import re
from typing import Any, Dict, List

from models import Schedule, ScheduleConfig


def _offset_cron(cron: str, offset_hours: int) -> str:
    """Offset a cron schedule's hour field by the given hours."""
    parts = cron.split()
    if len(parts) != 5:
        return cron

    hour_part = parts[1]
    # Handle "H/N" or "H" format
    match = re.match(r"(\d+)(/\d+)?", hour_part)
    if match:
        base = int(match.group(1))
        suffix = match.group(2) or ""
        new_base = (base + offset_hours) % 24
        parts[1] = f"{new_base}{suffix}"

    return " ".join(parts)


def schedule_to_template_data(
    schedule: Schedule,
    config: ScheduleConfig,
    cron_override: str = None,
) -> Dict[str, Any]:
    """
    Convert a Schedule into the JSON structure expected by the Liquid template.

    Template expects:
    {
      "schedule": "0 3/12 * * *",
      "queues": ["citrine1", ...],
      "groups": [
        {
          "jobs": [
            {
              "name": "Scenario Pod",
              "template": "scenario.yml",
              "profiles": ["sut-profile", "load-profile"],
              "timeout": 120
            }
          ]
        }
      ]
    }
    """
    groups = []
    for stage in schedule.stages:
        jobs = []
        for run in stage.runs:
            jobs.append({
                "name": run.name,
                "template": run.scenario.template,
                "profiles": run.profiles,
                "timeout": max(120, int(run.estimated_runtime * 2)),
            })
        groups.append({"jobs": jobs})

    return {
        "schedule": cron_override or config.schedule,
        "queues": config.queues,
        "groups": groups,
    }


def render_with_liquid(template_path: str, data: Dict[str, Any]) -> str:
    """Render a Liquid template with the given data."""
    try:
        from liquid import Environment
        env = Environment()
        with open(template_path, "r") as f:
            template_str = f.read()
        template = env.from_string(template_str)
        return template.render(**data)
    except ImportError:
        # Fallback: manual rendering for the simple template structure
        return _render_simple(template_path, data)


def _render_simple(template_path: str, data: Dict[str, Any]) -> str:
    """
    Simple template renderer that generates YAML directly without Liquid.
    Produces the same output as the Liquid template.
    """
    lines = []
    lines.append("# Do not change this file, it is generated by the pod-scheduler.")
    lines.append("# To regenerate, run:")
    lines.append("# python ./scripts/pod-scheduler/main.py "
                 "--config ./build/<config>.json "
                 "--template ./build/benchmarks.template.liquid "
                 "--yaml-output ./build")
    lines.append("")
    lines.append("trigger: none")
    lines.append("pr: none")
    lines.append("")
    lines.append("schedules:")
    lines.append(f'- cron: "{data["schedule"]}"')
    lines.append("  always: true")
    lines.append("  branches:")
    lines.append("    include:")
    lines.append("    - main")
    lines.append("")
    lines.append("variables:")
    lines.append("  - template: job-variables.yml")
    lines.append("  - name: session")
    lines.append("    value: $(Build.BuildNumber)")
    lines.append("  - name: buildId")
    lines.append("    value: $(Build.BuildId)")
    lines.append("  - name: buildNumber")
    lines.append("    value: $(Build.BuildNumber)")
    lines.append("  - name: am")
    lines.append("    value: $[lt(format('{0:HH}', pipeline.startTime), 12)]")
    lines.append("  - name: pm")
    lines.append("    value: $[ge(format('{0:HH}', pipeline.startTime), 12)]")
    lines.append("")
    lines.append("jobs:")
    lines.append("")

    queues = data["queues"]
    prev_group_jobs = []

    for group_idx, group in enumerate(data["groups"]):
        group_num = group_idx + 1
        lines.append(f"# GROUP {group_num}")
        lines.append("")

        current_jobs = []
        for job_idx, job in enumerate(group["jobs"]):
            job_id = job["name"].replace(" ", "_").replace("-", "_")
            current_jobs.append(job_id)
            queue = queues[job_idx % len(queues)]
            timeout = job.get("timeout", 120)
            depends = ", ".join(prev_group_jobs) if prev_group_jobs else ""

            profiles_args = " ".join(
                f"--profile {p}" for p in job["profiles"]
            )

            lines.append(f"- job: {job_id}")
            lines.append(f"  displayName: {group_num}- {job['name']}")
            lines.append("  pool: server")
            lines.append(f"  timeoutInMinutes: {timeout}")
            lines.append(f"  dependsOn: [{depends}]")
            lines.append("  condition: succeededOrFailed()")
            lines.append("  steps:")
            lines.append(f"  - template: {job['template']}")
            lines.append("    parameters:")
            lines.append("      connection: ASPNET Benchmarks Service Bus")
            lines.append(f"      serviceBusQueueName: {queue}")
            lines.append("      serviceBusNamespace: aspnetbenchmarks")
            lines.append(
                f'      arguments: "$(ciProfile) {profiles_args} "'
            )
            lines.append("")

        prev_group_jobs = current_jobs

    return "\n".join(lines) + "\n"


def generate_yamls(
    schedules: List[Schedule],
    config: ScheduleConfig,
    template_path: str,
    output_dir: str,
    base_name: str = "benchmarks-ci",
) -> List[str]:
    """Generate YAML pipeline files for each sub-schedule."""
    os.makedirs(output_dir, exist_ok=True)
    output_files = []

    for i, sched in enumerate(schedules):
        suffix = f"-{i + 1:02d}" if len(schedules) > 1 else ""
        filename = f"{base_name}{suffix}.yml"
        filepath = os.path.join(output_dir, filename)

        cron = _offset_cron(
            config.schedule, config.schedule_offset_hours * i
        )
        data = schedule_to_template_data(sched, config, cron_override=cron)
        yaml_content = _render_simple(template_path, data)

        with open(filepath, "w", newline="\n") as f:
            f.write(yaml_content)

        output_files.append(filepath)
        print(f"  Generated: {filepath}")

    return output_files
